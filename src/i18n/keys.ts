/**
 * Exported translation key map.
 *
 * `K` is a const object of dotted-path keys (e.g. `K.eq.title === "eq.title"`)
 * that other agents (eq-ui, presets, firmware, auth) can import to call
 * `t(K.eq.title)` without hand-typing string literals. The keys mirror the
 * structure of the resource bundles in `./locales`.
 *
 * `TranslationKey` is the union of every valid dotted path, derived from the
 * zh bundle, so referencing a non-existent key is a compile error.
 */

import type { ZhResources } from "./locales/zh";

/** Recursively flatten a nested resource object into dotted-path string keys. */
type DottedKeys<T, Prefix extends string = ""> = {
  [K in keyof T & string]: T[K] extends Record<string, unknown>
    ? DottedKeys<T[K], `${Prefix}${K}.`>
    : `${Prefix}${K}`;
}[keyof T & string];

/** Union of every valid translation key, e.g. "eq.title" | "auth.login" | ... */
export type TranslationKey = DottedKeys<ZhResources>;

/** Top-level namespaces present in the bundles. */
export type Namespace = keyof ZhResources;

